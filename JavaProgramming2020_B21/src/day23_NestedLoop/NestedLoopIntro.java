package day23_NestedLoop;

public class NestedLoopIntro {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 10; j++) {
                System.out.print(j + " ");
            }
        }
        System.out.println("=========================");
        for (int n = 1; n <= 10; n++){
            for (int i = 1; i <= 20; i++){
                System.out.print("*");
            }
            System.out.println();
        }






    }
}
