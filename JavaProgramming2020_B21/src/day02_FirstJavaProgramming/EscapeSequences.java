package day02_FirstJavaProgramming;

public class EscapeSequences {
    public static void main(String[] args) {
        //\n:
        System.out.println("Hello Everyone");
        System.out.println("My name is Veronica Pak. I am from Worderworld!");
        System.out.println("==================================================");
        System.out.println("Hello Everyone\nMy name is Veronica Pak. I am from Worderworld!");

        //\t:
        System.out.println("\tToday I learned Java");
        System.out.println("It was so much fun");

        // \\:
        System.out.println("/");
        System.out.println("\\");
        System.out.println("==================================================");

        // \'
        System.out.println("My name is 'Veronica'");
        System.out.println("My name is \'Veronice\'");
        System.out.println("====================================================");

        // \"
        System.out.println("My favorite show is Friends");
        System.out.println("My favorite show is \"Friends\"");




    }
}
