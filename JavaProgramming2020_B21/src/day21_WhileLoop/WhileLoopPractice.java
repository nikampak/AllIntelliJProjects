package day21_WhileLoop;

public class WhileLoopPractice {
    public static void main(String[] args) {
        for (int i = 1; i <=10; i++){
            System.out.print(i);
        }
        System.out.println();
        System.out.println("=======================");
        int j = 1;
        while (j <=10){
            System.out.print(j);
            j++;
        }
    }
}
